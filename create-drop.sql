		CHECK (REGEXP_LIKE(V_CPF, '((\d{3}).(\d{3}).(\d{3})-(\d{2}))')),
    
	CONSTRAINT CK_NOME_APENAS_LETRAS
    	CHECK (REGEXP_LIKE(V_NOME, '[ a-zA-Z áéíóúâêîôûã]')),
    
