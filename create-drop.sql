    CONSTRAINT CK_CEP
    	CHECK (REGEXP_LIKE(V_CEP_ENDERECO, '(^\d{5})\-(\d{3}$)')),
    	
    CONSTRAINT CK_TIPO_DE_VOLUNTARIO 
    	CHECK (UPPER(V_TIPO_VOLUNTARIO) IN ('MOT', 'DIV', 'INF', 'ORG', 'COO'))
);

