CREATE TRIGGER INSERIR_VOLUNTARIO_DADO_TIPO
	AFTER INSERT ON VOLUNTARIO
	FOR EACH ROW 
DECLARE
BEGIN
	IF :NEW.V_TIPO_VOLUNTARIO = 'INF' THEN INSERT INTO INFRAESTRUTURA  ( I_CPF ) VALUES (:NEW.V_CPF); END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'COO' THEN INSERT INTO COORDENACAO     ( C_CPF ) VALUES (:NEW.V_CPF); END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'MOT' THEN INSERT INTO MOTORISTA	   ( M_CPF ) VALUES (:NEW.V_CPF); END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'DIV' THEN INSERT INTO DIVULGACAO 	   ( D_CPF ) VALUES (:NEW.V_CPF); END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'ORG' THEN INSERT INTO ORGANIZACAO     ( O_CPF ) VALUES (:NEW.V_CPF); END IF;
END;

CREATE TRIGGER DELETAR_VOLUNTARIO_DADO_TIPO
	AFTER DELETE ON VOLUNTARIO
	FOR EACH ROW 
DECLARE
BEGIN
	IF :NEW.V_TIPO_VOLUNTARIO = 'INF' THEN DELETE FROM INFRAESTRUTURA  WHERE I_CPF = :NEW.V_CPF; END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'COO' THEN DELETE FROM COORDENACAO     WHERE C_CPF = :NEW.V_CPF; END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'MOT' THEN DELETE FROM MOTORISTA	   WHERE M_CPF = :NEW.V_CPF; END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'DIV' THEN DELETE FROM DIVULGACAO 	   WHERE D_CPF = :NEW.V_CPF; END IF;
	IF :NEW.V_TIPO_VOLUNTARIO = 'ORG' THEN DELETE FROM ORGANIZACAO     WHERE :NEW.V_CPF = O_CPF; END IF;
END;



